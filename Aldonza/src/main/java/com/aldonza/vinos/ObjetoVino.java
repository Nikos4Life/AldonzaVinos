public class ObjetoVino {
    private int id;
    private String Tipovino;
    private String Tipouva;
    private String Presentacion;
    private String Graduacion;
    private String Elaboracion;
    private String Gastronomia;
    private String Servicio;
    private String Produccion;
    private String NotasCata;
    private String Recomendacion;

    public ObjetoVino() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTipovino() {
        return Tipovino;
    }

    public void setTipovino(String tipovino) {
        Tipovino = tipovino;
    }

    public String getTipouva() {
        return Tipouva;
    }

    public void setTipouva(String tipouva) {
        Tipouva = tipouva;
    }

    public String getPresentacion() {
        return Presentacion;
    }

    public void setPresentacion(String presentacion) {
        Presentacion = presentacion;
    }

    public String getGraduacion() {
        return Graduacion;
    }

    public void setGraduacion(String graduacion) {
        Graduacion = graduacion;
    }

    public String getElaboracion() {
        return Elaboracion;
    }

    public void setElaboracion(String elaboracion) {
        Elaboracion = elaboracion;
    }

    public String getGastronomia() {
        return Gastronomia;
    }

    public void setGastronomia(String gastronomia) {
        Gastronomia = gastronomia;
    }

    public String getServicio() {
        return Servicio;
    }

    public void setServicio(String servicio) {
        Servicio = servicio;
    }

    public String getProduccion() {
        return Produccion;
    }

    public void setProduccion(String produccion) {
        Produccion = produccion;
    }

    public String getNotasCata() {
        return NotasCata;
    }

    public void setNotasCata(String notasCata) {
        NotasCata = notasCata;
    }

    public String getRecomendacion() {
        return Recomendacion;
    }

    public void setRecomendacion(String recomendacion) {
        Recomendacion = recomendacion;
    }
}
